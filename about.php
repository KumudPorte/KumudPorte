  <p style=" font-size:25px; color:white;">We have seen that the inception of Internet has dramatically revolutionized many fields. Internet has made life of people 
      so easy that people today have access to any information they want sitting at their home. One of the main fields that
       Internet has revolutionized is communication. And talking about communication over Internet, the first thing that comes 
       in our mind is E-mail. E-mails are considered to be the most reliable way of communication over Internet, for sending
        or receiving some important information. But there is a special criteria for humans to access the Internet and the
         criteria is you must be able to see. You must be thinking that what sort of criteria is this, every one with eyes can
          see. But there are also specially abled people in our society who are not gifted with what you have. Yes there are 
          some visually challenged people or blind people who can not see things and thus can not see the computer screen or 
          keyboard.
A survey shows that there are more than 250 million visually challenged people around the globe. That is, around 250 
million people are unaware of how to use Internet or E-mail. The only way by which a visually impaired person can send an E-mail
 they have to dictate the entire content of the mail to a third person( not visually challenged ) and then the third person
  will compose the mail and send on the behalf of the visually impaired person.
But this is not a correct way to deal with this problem. It is very less likely that every time a visually challenged
 person can find someone for help. Although for these reasons the specially abled people are criticized by our society.
So, for the betterment of society and giving an equal status to such specially abled people we have come up with this project
 idea which provides the user with ability
 to send mails using voice commands without the need of keyboard or any other visual things.</p>
<p style=" font-size:25px; color:white;">As the title suggests, this applicationis a web-based application for visually impaired persons
     using IVR- Interactive voice response, thus enabling everyone to control their mail accounts using 
     their voice only and to be able to read,send, and perform all the other useful tasks. The system will 
     prompt the user with voice commands to perform certain action and the user will respond to the same.
      The main benefit of this system is that the use of keyboard is completely eliminated,
     the user will have to respond through voice and mouse click only.
    </p>
    <p style=" font-size:25px; color:white;">This project is proposed for the betterment of society. 
    This project aims to help the visually impaired people to be a part of growing digital India by
     using internet and also aims to make life of such people quite easy. Also, the success of this project will 
     also encourage developers to build something more useful for visually
     impaired or illiterate people, who also deserves an equal standard in society.</p>

